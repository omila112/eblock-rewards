const contract = new web3.eth.Contract(contractABI, contractAddress);

const ownerPrivateKey = '0xd19fe9521e38239d011fb35217524c8f73f227daa9ee38d9bcf68ea930c63649'; 

app.post('/api/reward', async (req, res) => {
    const { user, weight } = req.body;
    console.log(`♻️  Recycling! User: ${user}, Weight: ${weight}kg`);

    try {
        
        const ownerAccount = web3.eth.accounts.privateKeyToAccount(ownerPrivateKey);

        const rewardAmount = Math.floor(weight * 50); 
        
        
        const tx = contract.methods.issueReward(user, Math.floor(weight));
        
        // 3. Estimate Gas (using the correct FROM address)
        const gas = await tx.estimateGas({ from: ownerAccount.address });
        
        // 4. Sign Transaction (Explicitly adding 'from')
        const signedTx = await web3.eth.accounts.signTransaction(
            {
                from: ownerAccount.address, // <--- THIS WAS MISSING
                to: contractAddress,
                data: tx.encodeABI(),
                gas: gas,
                gasPrice: await web3.eth.getGasPrice()
            },
            ownerPrivateKey
        );

        // 5. Send Transaction
        const receipt = await web3.eth.sendSignedTransaction(signedTx.rawTransaction);
        
        console.log(`✅ Reward Sent! TX: ${receipt.transactionHash}`);
        res.json({ success: true, tx: receipt.transactionHash, tokens: rewardAmount });

    } catch (error) {
        console.error(error);
        res.status(500).json({ error: error.message });
    }
});

// GET USER BALANCE FROM BLOCKCHAIN
app.get('/api/balance/:address', async (req, res) => {
    try {
        const userAddress = req.params.address;
        
        // Connect to your deployed contract
        const contract = new web3.eth.Contract(contractABI, contractAddress);
        
        // Call the built-in ERC20 balanceOf function
        const balanceWei = await contract.methods.balanceOf(userAddress).call();
        
        // Convert from 18 decimal format to standard format
        const balanceEBR = web3.utils.fromWei(balanceWei, 'ether');
        
        // Send the number back to the Flutter app!
        res.json({ balance: balanceEBR });
        
    } catch (error) {
        console.error("Error fetching balance:", error);
        res.status(500).json({ error: error.message });
    }
});

